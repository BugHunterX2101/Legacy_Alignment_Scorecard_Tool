export const getAllGlobalCompanies = (): string[] => {
  const allCompanies: string[] = [];
  Object.values(globalCompanies).forEach(countryCompanies => {
    allCompanies.push(...countryCompanies);
  });
  return allCompanies.sort();
};

export const getCompaniesByCountry = (country: string): string[] => {
  return globalCompanies[country] || [];
};

export const getRandomGlobalCompanies = (count: number = 50): string[] => {
  const allCompanies = getAllGlobalCompanies();
  const shuffled = allCompanies.sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export const searchGlobalCompanies = (query: string, limit: number = 20): string[] => {
  const allCompanies = getAllGlobalCompanies();
  return allCompanies
    .filter(company => company.toLowerCase().includes(query.toLowerCase()))
    .slice(0, limit);
};